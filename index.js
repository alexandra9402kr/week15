function backgroundColor() {
    if (document.getElementById("color").value == "blue") {
        document.getElementById("background-color").style.backgroundColor='blue';
    }
    if (document.getElementById("color").value == "yellow") {
        document.getElementById("background-color").style.backgroundColor='yellow';
    }
    if (document.getElementById("color").value == "white") {
        document.getElementById("background-color").style.backgroundColor='white';
    }
}